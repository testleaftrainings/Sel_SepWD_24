package week1.day4;

public class LearnForloop {

	public static void main(String[] args) {
		// Create for loop
		//shortcut to create for loop
		// type for -> ctrl+space -> select from the suggestion
		
		//For loop
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}

		System.out.println("--------------------");
		
		
		//Reverse for loop
		
		for(int i=5;i>=1;i--) {
			System.out.println(i);
		}
		
	}

}
