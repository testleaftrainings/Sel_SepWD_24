package week1.day2;

public class LearnElseIf {

	public static void main(String[] args) {
		
		int a=20;
		int b=20;
		
		if(a>b) {
			System.out.println("A is greater");
		}
		
		else if(a<b) {
			System.out.println("B is greater");
		}
		else {
			System.out.println("Both are equal");
		}
		
	}

}
